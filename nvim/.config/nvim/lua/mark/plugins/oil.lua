          max_width = 0,
          max_height = 0,
          border = "rounded",
          win_options = {
            winblend = 0,
          },
        },
      })

      -- <leader>pv to open Oil
      vim.keymap.set("n", "<leader>pv", require("oil").open, { desc = "Open Oil file browser" })
    end,
  },
}
